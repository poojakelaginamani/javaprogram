package com.xworkz.inheritance;

public class Institute {
      String hrName;
      int noOfStaff;
      int noOfClassRoom;
      String location;
      
 public Institute(String hrName, int noOfStaff, int noOfClassRoom, String location) {
		
		this.hrName = hrName;
		this.noOfStaff = noOfStaff;
		this.noOfClassRoom = noOfClassRoom;
		this.location = location;
	}
}
